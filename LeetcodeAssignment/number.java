package LeetcodeAssignment;
// Write a program using if-else to determine whether a number is positive, negative, or
// zero.
public class number {
    public static void main(String[] args) {
        int number = 5;
        if(number>0){
            System.out.println("positive");
        }else if (number<0){
            System.out.println("Negative");
        }
        else {
            System.out.println("Zero");
        }
    }
}
